package com.example.elective.models;

public class Role {
  private int id;
  private String name;

  public int getId() {
    return id;
  }

  public Role setId(int id) {
    this.id = id;
    return this;
  }

  public String getName() {
    return name;
  }

  public Role setName(String name) {
    this.name = name;
    return this;
  }

}
